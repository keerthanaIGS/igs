                      <b>How does LiDAR work?</b><br>
                      Most LiDAR systems consist of four elements:<br>

                        1) <b>Laser:</b> Sends pulses of light towards objects (usually ultraviolet or near-infrared).<br><br>
                        
                        2) <b>Scanner:</b> Regulates the speed at which the laser can scan target objects and the maximum distance reached by the laser.
                            <br><br>
                        3) <b>Sensor:</b> Measures the time it takes for the light from the laser to bounce off the target object
                            and return to the system (thereby measuring distance).
                        <br><br>
                        4) <b>GPS:</b> Tracks the location of the LiDAR system to ensure accuracy of the distance measurements.
