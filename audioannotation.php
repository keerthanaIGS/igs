                        <b>Speech Annotation Quality Assessment</b><br>
                        It aims to determine the accuracy of the speech annotations, including word error rate (substitutions, insertions and deletions), and label error rate according to the annotation guidelines.
                        
                        It helps assess the quality of the annotated speech in terms of accuracy and interpretation consistency of the annotation guidelines. It also helps complete the guidelines and resolve its ambiguities.
                        <br><br>
                        <b>Speech Database Quality Assessment</b><br>
                        This quality assessment service provides information that helps optimize the effort in data collection and annotation. Quality is a multidimensional parameter that depends on factors such as the volume and quality of the audio, the accuracy of the annotations, the data consistency, the domain and customer’s coverage; and the balance.
                        
                        This helps focus the data collection and annotation effort where it is most needed.
                        <br><br>
                        <b>Intelligent Assistants Assessment</b><br>
                        This service measures the performance of the wake word detection, assesses if the pronunciation of the wake word and the subsequent voice commands belong to the same or several users, checks if the voice interactions are in the expected language, or if the answers of the assistant are correct based on the dialogue status and context as well as on the user data and the system knowledge database, etc.
                        <br><br>
                        <b>Pronunciation Assessment</b><br>
                        The pronunciation assessment aims to determine whether the pronunciation of a word or sentence is correct. The correctness of the pronunciation can be performed by comparing it with the standard pronunciation or with the dialect variants.
                        The pronunciation assessment can be performed on human or synthetic speech.

